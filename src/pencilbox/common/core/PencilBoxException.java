package pencilbox.common.core;

/**
 * PencilBox用例外クラス
 */
public class PencilBoxException extends Exception {

	/**
	 * 
	 */
	public PencilBoxException() {
		super();
	}

	/**
	 * @param arg0
	 */
	public PencilBoxException(String arg0) {
		super(arg0);
	}

	/**
	 * @param arg0
	 */
	public PencilBoxException(Throwable arg0) {
		super(arg0);
	}
}
